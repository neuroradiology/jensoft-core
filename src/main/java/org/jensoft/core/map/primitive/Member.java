package org.jensoft.core.map.primitive;

public class Member {

    private String type;
    private String ref;
    private String role;

    public Member(String ref, String role, String type) {
        super();
        this.ref = ref;
        this.role = role;
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getRef() {
        return ref;
    }

    public void setRef(String ref) {
        this.ref = ref;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

}
