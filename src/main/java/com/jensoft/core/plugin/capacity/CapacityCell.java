package com.jensoft.core.plugin.capacity;

public class CapacityCell {

    private int column;
    private int row;

    public CapacityCell(int row, int column) {
        super();
        this.row = row;
        this.column = column;
    }

    public int getColumn() {
        return column;
    }

    public void setColumn(int column) {
        this.column = column;
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

}
